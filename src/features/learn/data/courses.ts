// Helper functions
export function getCoursesByCategory(category: string): Course[] {
  return MOCK_COURSES.filter((c) => c.category === category);
}

export function getCoursesByLevel(level: CourseLevel): Course[] {
  return MOCK_COURSES.filter((c) => c.level === level);
}

export function getFreeCourses(): Course[] {
  return MOCK_COURSES.filter((c) => c.level === 1);
}

export function getFeaturedCourses(): Course[] {
  return MOCK_COURSES.filter((c) => c.isFeatured);
}

export function getCourseById(id: string): Course | undefined {
  return MOCK_COURSES.find((c) => c.id === id);
}

export function getAllCourses(): Course[] {
  return MOCK_COURSES;
}

export function getRecentCourses(limit: number = 10): Course[] {
  return [...MOCK_COURSES]
    .sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime())
    .slice(0, limit);
}

export function searchCourses(query: string): Course[] {
  const lowered = query.toLowerCase();
  return MOCK_COURSES.filter(
    (c) =>
      c.title.toLowerCase().includes(lowered) ||
      c.description.toLowerCase().includes(lowered) ||
      c.tags.some((t) => t.toLowerCase().includes(lowered))
  );
}
