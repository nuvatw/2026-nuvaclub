// Helper to create lessons with YouTube video IDs
function createLessons(
  courseId: string,
  lessonData: { title: string; videoUrl: string; duration: number }[]
) {
  return lessonData.map((l, i) => ({
    id: `${courseId}-l${i + 1}`,
    title: l.title,
    duration: l.duration,
    videoUrl: l.videoUrl,
    order: i + 1,
  }));
}

