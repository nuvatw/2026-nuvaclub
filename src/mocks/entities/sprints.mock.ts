// Helper functions
export const getSprintById = (id: string): Sprint | undefined =>
  MOCK_SPRINTS.find((s) => s.id === id);

export const getSprintsBySeasonId = (seasonId: string): Sprint[] =>
  MOCK_SPRINTS.filter((s) => s.seasonId === seasonId);

export const getActiveVotingSprints = (): Sprint[] =>
  MOCK_SPRINTS.filter((s) => s.isVotingOpen);

export const getSprintsByTheme = (theme: string): Sprint[] =>
  MOCK_SPRINTS.filter((s) => s.theme.toLowerCase() === theme.toLowerCase());

export const getCurrentSprints = (): Sprint[] => {
  const now = new Date();
  return MOCK_SPRINTS.filter((s) => s.startDate <= now && s.endDate >= now);
};

export const getRecentSprints = (count: number = 4): Sprint[] =>
  [...MOCK_SPRINTS].sort((a, b) => b.startDate.getTime() - a.startDate.getTime()).slice(0, count);
